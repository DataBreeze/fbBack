<div class="abBox1">FishBlab Research - Saltwater Fish Species</div>

<p>All Data in this section is provided by <a href="http://www.st.nmfs.noaa.gov/st1/recreational/overview/overview.html" target="NEW">NOAA Fisheries</a>.</p>

<p>FishBlab Saltwater Fish Species displays <b>one</b> Fish Species at a time and <b>all</b> of the corresponding Spots where they were caught. Along with information about the Fish Species is the popularity (catch count). This data was provided by <a href="/" onclick="closeDialog();pageShow('fish_data');return false;">NOAA Fisheries Recreational Fishing Surveys</a> and various other sources. There are 3 main tab sections, Fish Species and NOAA Top Spots.

<p>When you first search for a Fish Species, the system displays all Spots where the Fish has been caught. This demonstrates the range of the Fish Species</p>

<p>Like other FishBlab.com pages, the data on this page changes when the map is dragged, zoomed or searched. If you want to find out what is going on in a certain area, search or drag the Map to that location. If you don't see any data in the area you are browsing, try zooming the map out to cover more area. At this time FishBlab only contains data for US saltwater Fish Species.</p>


