<h2>Overview</h2>
<p>
FishBlab.com contains recreational fish catch data from the United States Federal government <a href="http://www.st.nmfs.noaa.gov/st1/recreational/overview/overview.html">NOAA Fisheries</a>. This data comes from surveys given at various marine locations around the US. The surveys were started in 1982 and continue to the present. NOAA uses this (and other) information to estimate the total recreational catch for a given year for each species. This allows analysis of the pressure put on fish stocks by recreational fishermen. From this analysis decisions about possible fishing restrictions can be made.
</p>
<p>
FishBlab.com uses this data in a different way. We have highly refined the data to only show catch totals grouped by species, site and by month. A highly optimized database allows instant retrieval of real time fish catch data that is restricted by the current map viewport, month range selection and the (optional) fish species selected.
</p>

<h2>What Areas are covered?</h2>
<p>
The locations in FishBlab.com are provided with the survey data by NOAA. These locations are generally coastal areas. FishBlab.com fish catch data is currently US Marine (saltwater) only.
</p>

<h2>How Accurate is the Fish Catch Data?</h2>
<p>
The recreational fish catch survey data used by FishBlab.com <i>should be</i> extremely accurate. Each survey involves a government inspector at a specific location who actually witnesses the catch. Most of the time the catch is measured and weighed as well. Although mistakes can happen, the integrity of the data can be expected to be excellent. 
</p>

<h2>What time period does the data cover?</h2>
<p>
 The recreational fish catch data has been collected since 1982 and continues to this day. Since that date data has been reported on a bi-monthly duration. Some years have more surveys than others and methods have changed over the years. At this point, FishBlab utilizes ALL of the data from all years to provide statistics on fishing areas and species. FishBlab.com allows the entire dataset over all years to be queried by a month range.
</p>

